// 名前と都道府県コードで一致するか判定
function matchesTier(castle, list) {
  return list.some(t => {
    const sameOrInclude = castle.name === t.name
      || castle.name === t.name + '跡'
      || castle.name.startsWith(t.name + ' ')
      || castle.name.startsWith(t.name + '（')
    const samePref = castle.prefecture_code === t.pref
    return sameOrInclude && samePref
  })
}

function assignTier(castle) {
  if (matchesTier(castle, TIER_S)) return 'S'
  if (matchesTier(castle, TIER_A)) return 'A'
  if (matchesTier(castle, TIER_B)) return 'B'
  return 'C'
}

const castles = JSON.parse(readFileSync('lib/spots/castle.json', 'utf-8'))
const result = castles.map(c => ({ ...c, tier: assignTier(c) }))

const counts = { S: 0, A: 0, B: 0, C: 0 }
result.forEach(c => counts[c.tier]++)
console.log('tier別集計:', counts)
